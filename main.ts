controller.anyButton.onEvent(ControllerButtonEvent.Pressed, function () {
    if (gamemode == "ufo") {
        mySprite.setVelocity(0, -75)
        pause(275)
        mySprite.setVelocity(0, 75)
    } else if (gamemode == "cube") {
        if (life == 1) {
            cubejump()
        }
    }
    if (gamemode == "ship") {
        mySprite.setVelocity(0, -35)
    } else if (gamemode == "wave") {
        mySprite.setVelocity(0, -50)
        mySprite.setImage(assets.image`myImage5`)
    } else if (gamemode == "ball") {
        if (updown == 2) {
            updown = 0
            mySprite.setVelocity(0, -100)
            pause(750)
            mySprite.setVelocity(0, 0)
            updown = 1
        } else if (updown == 1) {
            updown = 0
            mySprite.setVelocity(0, 100)
            pause(750)
            mySprite.setVelocity(0, 0)
            updown = 2
        }
    }
})
function cubejump () {
    mySprite.setVelocity(0, -68)
    pause(500)
    mySprite.setVelocity(0, 68)
    pause(500)
    mySprite.setVelocity(0, 0)
    if (controller.anyButton.isPressed()) {
        cubejump()
    } else {
        mySprite.setVelocity(0, 0)
    }
}
